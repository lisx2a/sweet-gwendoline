<p><span>Q. What is a CVN code?</span></p>

<p>A. For additional security, credit card companies place an individual security code on your credit card. This CVN (Card Verification Number) serves as an extra security precaution, and helps prevent fraudulent transactions.  On MasterCard and Visa cards, this number is recorded on the back of the card, near the signature panel and it normally consists of three digits that appear after the last four digits of your card number.</p>

